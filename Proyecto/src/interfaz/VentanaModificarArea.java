      public void cargarLista() {
        DefaultListModel<String> modelo = new DefaultListModel<>();
        ArrayList<Area> area = s.getAreas();
        for (Area a : area) {
            modelo.addElement(a.toString());
        }
        lstAreas.setModel(modelo);
    }
      
    private void recargarCampos(){
        DefaultListModel<String> modelo2 = new DefaultListModel<>();
        ArrayList<Empleado> empleado = seleccionada.getLst_empleados();
        for (Empleado e : empleado) {
            modelo2.addElement(e.toString());
        }
        lstEmpleados.setModel(modelo2);
        txtNombre.setText(seleccionada.getNombre());
        txtPresupuesto.setText(seleccionada.getPresupuestoAsignado()+"");
    }
    
