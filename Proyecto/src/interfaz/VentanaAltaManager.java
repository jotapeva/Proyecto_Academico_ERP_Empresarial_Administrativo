    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
    String nombre = txtNombre.getText().trim();
    String ci = txtCi.getText().trim();
    String celular = txtCelular.getText().trim();
    int antiguedad = (int) txtAntiguedad.getValue();
    double salario = ((Number) txtSalario.getValue()).doubleValue();
    Area area = s.buscarAreaPorNombre(cmbArea.getSelectedItem().toString());
    if (nombre.isEmpty() || ci.isEmpty() || celular.isEmpty() || area == null || salario <= 0 || s.buscarEmpleadoPorCI(ci) != null || s.buscarManagerPorCI(ci) != null || !area.hayPresupuestoSuficiente(salario)) {
        JOptionPane.showMessageDialog(this, "Error al agregar el manager. Verifique que todos los campos estén completos, la CI sea única y que haya presupuesto suficiente en el área.");
       
    }else{
    Manager m = new Manager(nombre, ci, celular, antiguedad);
    m.setSalario_mensual(salario);
    s.altaManager(m);
    s.agregarManagerArea(m, area);
    JOptionPane.showMessageDialog(this, "Manager agregado correctamente y asignado al área.");
    cargarListaCombo();   
    }
    }//GEN-LAST:event_btnIngresarActionPerformed

