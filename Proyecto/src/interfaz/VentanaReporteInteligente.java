    private void cargarTodosLosEmpleados() {
        modeloEmpleados.clear();
        for (Empleado e : s.getEmpleados()) {
            modeloEmpleados.addElement(e);
        }
    }

    private void cargarAreasDestino() {
        cmbAreaDestino.removeAllItems();
        for (Area a : s.getAreas()) {
            cmbAreaDestino.addItem(a.getNombre());
        }
    }

