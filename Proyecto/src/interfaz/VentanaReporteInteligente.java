    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        Area origen = s.buscarAreaPorNombre(lblAreaOrigen.getText());
        Area destino = s.buscarAreaPorNombre(cmbAreaDestino.getSelectedItem().toString());
        Empleado emp = lstEmpleados.getSelectedValue();
        if (origen == null || destino == null || emp == null) {
            JOptionPane.showMessageDialog(this, "Seleccione el área de origen, destino y un empleado.");
        } else {
            lblEstado.setText("Consultando IA...");
            lblReloj.setIcon(new ImageIcon(getClass().getResource("/imagenes/reloj.gif")));
            txtResultado.setText("");
            // Creamos el Thread ya que Swing no corria al gif al mismo tiempo que consultara la IA
            //Entonces entre ChatGpt y la pagina de W3Schools (https://www.w3schools.com/java/java_threads.asp) logramos hacerlo
            new Thread(() -> {
                String resultado = s.generarReporteInteligente(origen, destino, emp);
                // Volvemos al hilo principal
                SwingUtilities.invokeLater(() -> {
                    ImageIcon icono = new ImageIcon(getClass().getResource("/imagenes/check.png"));
                    Image imgEscalada = icono.getImage().getScaledInstance(30, 30, Image.SCALE_SMOOTH);
                    lblReloj.setIcon(new ImageIcon(imgEscalada));
                    lblEstado.setText("Reporte listo:");
                    txtResultado.setText(resultado);
                });
            }).start();
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

    private void lstEmpleadosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEmpleadosValueChanged
        if (!evt.getValueIsAdjusting()) {
            Empleado seleccionado = lstEmpleados.getSelectedValue();
            if (seleccionado != null && seleccionado.getArea() != null) {
                lblAreaOrigen.setText(seleccionado.getArea().getNombre());
            } else {
                lblAreaOrigen.setText("(sin área)");
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_lstEmpleadosValueChanged

