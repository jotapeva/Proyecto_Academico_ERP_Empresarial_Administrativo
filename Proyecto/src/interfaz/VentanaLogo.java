    private void iniciarAnimacion() {
        cargarImagen();
        pbTimer.setValue(0);
        progreso = 0;

        timer = new Timer(30, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                progreso++;
                pbTimer.setValue(progreso);

                if (progreso >= 100) {
                    timer.stop();
                    dispose();
                    new VentanaBienvenida(s).setVisible(true);
                }
            }
        });

        timer.start();
    }
    // ---------- Mostrar imagen ----------
    private void cargarImagen() {
    try {
        ImageIcon icono = new ImageIcon(getClass().getResource("/imagenes/logo.jpg"));
        Image imgEscalada = icono.getImage().getScaledInstance(380, 250, Image.SCALE_SMOOTH);
        lblImagen.setIcon(new ImageIcon(imgEscalada));
        lblImagen.setHorizontalAlignment(JLabel.CENTER);
    } catch (Exception e) {
        lblImagen.setText("No se pudo cargar la imagen");
        lblImagen.setHorizontalAlignment(JLabel.CENTER);
    }
}

