    private void cargarLista() {
        DefaultListModel<String> modelo = new DefaultListModel<>();
        for (Manager m : s.getManagers()) {
            modelo.addElement(m.toString());
        }
        lstManagers.setModel(modelo);
    }
    private void mostrarDatos() {
        if (seleccionado != null) {
            txtNombre.setText(seleccionado.getNombre());
            txtCi.setText(seleccionado.getCi());
            txtCelular.setText(seleccionado.getCelular());
            txtAntiguedad.setText(seleccionado.getAntiguedad()+"");
            txtArea.setText(seleccionado.getArea().toString());
            txtCantidad.setText(String.valueOf(seleccionado.cantidadEmpleadosACargo()));
        }
    }

